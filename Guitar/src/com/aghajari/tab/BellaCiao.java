public class BellaCiao {

    public static Tablature create() {
        int gap = 330;
        int gap2 = 1000;
        int gap3 = 600;

        return new Tablature()
                .withListener((note, i) -> System.out.println(note))
                .add(0, 0, 3000)   // 1
                .add(1, 3, gap)
                .add(0, 0, gap)
                .add(0, 1, gap + 20)
                .add(0, 1, gap)
                .add(0, 1, gap2)    // 2
                .add(0, 0, gap)
                .add(1, 3, gap)
                .add(0, 1, gap + 20)
                .add(0, 0, gap)
                .add(0, 0, gap2)    // 3
                .add(1, 3, gap)
                .add(1, 1, gap)
                .add(1, 0, gap + 20)
                .add(0, 0, gap3)
                .add(1, 1, gap3)
                .add(1, 0, gap3)
                .add(2, 2, gap3)
                .add(3, 2, gap2)    // 4
                .add(2, 2, gap)
                .add(1, 0, gap)
                .add(1, 1, gap)
                .add(2, 2, gap)
                .add(3, 2, gap2)    // 5
                .add(2, 2, gap)
                .add(1, 0, gap)
                .add(1, 1, gap)
                .add(2, 2, gap)
                .add(3, 2, gap2)    // 6
                .add(2, 2, gap)
                .add(1, 0, gap)
                .add(1, 1, gap)
                .add(1, 0, gap3)
                .add(2, 2, gap)
                .add(1, 1, gap)
                .add(1, 0, gap3)
                .add(2, 2, gap)
                .add(0, 0, gap)
                .add(0, 0, gap3)    // 7
                .add(0, 0, gap3)
                .add(0, 0, gap - 50)
                .add(1, 3, gap)
                .add(0, 0, gap)
                .add(0, 1, gap)
                .add(0, 1, gap)
                .add(0, 1, gap2)    // 8
                .add(0, 0, gap)
                .add(1, 3, gap)
                .add(0, 1, gap + 20)
                .add(0, 0, gap)
                .add(0, 0, gap2)    // 9
                .add(1, 3, gap)
                .add(1, 1, gap)
                .add(1, 0, gap + 20)
                .add(0, 0, gap3)
                .add(1, 1, gap3)
                .add(1, 0, gap3)
                .add(2, 2, gap3);
    }

}
